package com.lccert.crm.client;

/**
 * 客户所属地区实体类
 * 记录客户所在地区信息
 * @author tangzp
 *
 */
public class AreaForm {

	private String city;

	private String code;

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
