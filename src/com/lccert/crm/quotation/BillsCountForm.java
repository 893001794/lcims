package com.lccert.crm.quotation;
/**
 * 签单的统计模块
 * @author lcc
 *
 */
public class BillsCountForm {
	private int sumYD;
	private int sumYM;
	private int sumND;
	private int sumNM;
	private int sumNMon;
	private int sumNMM;
	public int getSumYD() {
		return sumYD;
	}
	public void setSumYD(int sumYD) {
		this.sumYD = sumYD;
	}
	public int getSumYM() {
		return sumYM;
	}
	public void setSumYM(int sumYM) {
		this.sumYM = sumYM;
	}
	public int getSumND() {
		return sumND;
	}
	public void setSumND(int sumND) {
		this.sumND = sumND;
	}
	public int getSumNM() {
		return sumNM;
	}
	public void setSumNM(int sumNM) {
		this.sumNM = sumNM;
	}
	public int getSumNMon() {
		return sumNMon;
	}
	public void setSumNMon(int sumNMon) {
		this.sumNMon = sumNMon;
	}
	public int getSumNMM() {
		return sumNMM;
	}
	public void setSumNMM(int sumNMM) {
		this.sumNMM = sumNMM;
	}


}
